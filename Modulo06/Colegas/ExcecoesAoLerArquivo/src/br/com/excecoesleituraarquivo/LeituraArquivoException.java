package br.com.excecoesleituraarquivo;

/**
 * @author Raphael
 *
 */
public class LeituraArquivoException extends RuntimeException {

	private static final long serialVersionUID = 245562802303842254L;

	public LeituraArquivoException() {
	}

	public LeituraArquivoException(String arg0) {
		super(arg0);

	}

	public LeituraArquivoException(Throwable arg0) {
		super(arg0);

	}

	public LeituraArquivoException(String arg0, Throwable arg1) {
		super(arg0, arg1);

	}

	public LeituraArquivoException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);

	}

}
